<h3>Web de mensajería para melómanos</h3>
<h4>Nivel 1 <span class="hecho"> HECHO </span></h4>
<ul>
    <li>Acceso al sitio web mediante usuario y contraseña.</li>
    <li>Tipo de envío: de uno a todos.</li>
    <li>Cuando el usuario entre en la aplicación el usuario debe leer todos 
        los mensajes enviados. El tambi´en puede enviar mensajes.</li>
</ul>
<h4>Nivel 2 <span class="hecho"> HECHO </span></h4>
<ul>
    <li>Tipo de envío: de uno a otro (mensaje personal).</li>
    <li>Cuando el usuario entre en la aplicaci´on el usuario debe leer también 
        todos los mensajes personales. El también puede enviar mensajes personales.</li>
</ul>
<h4>Nivel 3 <span class="hecho"> HECHO </span></h4>
<ul>
    <li>Debe existir un perfil de los usuarios basado en el tipo de música preferida y en la edad.</li>
    <li>Cada grupo viene definido por un tipo de música y un rango de edades de sus miembros.</li>
    <li>Con esta información un administrador (a través de una página web aparte) debe poder dar de alta grupos.</li>
    <li>Cuando el usuario entre en la aplicación el usuario debe leer también todos los mensajes dirigidos a los 
        grupos a los que pertenezca. El también puede enviar mensajes a esos grupos.</li>
</ul>
<h5>NOTA: la interfaz es libre.</h5>